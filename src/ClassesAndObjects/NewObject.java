package ClassesAndObjects;

public class NewObject
{
	
	public void a()
	{
		System.out.println("hi");
	}
	public void b()
	{
		System.out.println("hello");
	}
	public void c()
	{
		System.out.println("krishna");
	}
	public static void main(String[] args)
	{
		NewObject no = new NewObject();
		no.a();
		no.b();
		no.c();

	}

}
